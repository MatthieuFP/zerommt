class ImgProjector(nn.Module):
    """Image feature projection layer."""
    def __init__(self, config):
        super().__init__()
        self.linear = nn.Linear(768, config.d_model)

    def forward(self, x):
        x = self.linear(x)
        return F.relu(x)


class MultimodalM2M100ForConditionalGeneration(M2M100ForConditionalGeneration):
    base_model_prefix = "model"
    _tied_weights_keys = ["encoder.embed_tokens.weight", "decoder.embed_tokens.weight", "lm_head.weight"]

    def __init__(self, config: M2M100Config):
        super().__init__(config)
        self.model = MultimodalM2M100Model(config)
        self.lm_head = nn.Linear(config.d_model, self.model.shared.num_embeddings, bias=False)

        self.adapter_config = BnConfig(mh_adapter=True, output_adapter=True, reduction_factor=8,
                                       non_linearity="relu")

        adapters.init(self.model)
        self.model.add_adapter("bottleneck_adapter", config=self.adapter_config)
        self.model.set_active_adapters("bottleneck_adapter")

        self.freeze_params()

    def freeze_params(self):
        for name, param in self.model.named_parameters():
            if not any([proj_name in name for proj_name in ["proj_img", "layer_norm_img", "img_embedding"]]) and "adapters" not in name:
                param.requires_grad = False

        self.lm_head.weight.requires_grad = False

    def prepare_inputs_for_generation(
        self,
        decoder_input_ids,
        past_key_values=None,
        attention_mask=None,
        head_mask=None,
        decoder_head_mask=None,
        cross_attn_head_mask=None,
        use_cache=None,
        encoder_outputs=None,
        **kwargs,
    ):
        # cut decoder_input_ids if past is used
        if past_key_values is not None:
            past_length = past_key_values[0][0].shape[2]

            # Some generation methods already pass only the last input ID
            if decoder_input_ids.shape[1] > past_length:
                remove_prefix_length = past_length
            else:
                # Default to old behavior: keep only final ID
                remove_prefix_length = decoder_input_ids.shape[1] - 1

            decoder_input_ids = decoder_input_ids[:, remove_prefix_length:]

        img_features = None
        if "input_visual_features" in kwargs:
            img_features = kwargs["input_visual_features"]

        return {
            "input_ids": None,  # encoder_outputs is defined. input_ids not needed
            "encoder_outputs": encoder_outputs,
            "past_key_values": past_key_values,
            "decoder_input_ids": decoder_input_ids,
            "attention_mask": attention_mask,
            "head_mask": head_mask,
            "decoder_head_mask": decoder_head_mask,
            "cross_attn_head_mask": cross_attn_head_mask,
            "use_cache": use_cache,  # change this to avoid caching (presumably for debugging)
            "input_visual_features": img_features
        }

    def _prepare_encoder_decoder_kwargs_for_generation(
        self, inputs_tensor: torch.Tensor, model_kwargs, model_input_name: Optional[str] = None
    ) -> Dict[str, Any]:
        # 1. get encoder
        encoder = self.get_encoder()
        # Compatibility with Accelerate big model inference: we need the encoder to outputs stuff on the same device
        # as the inputs.
        if hasattr(self, "hf_device_map"):
            if hasattr(encoder, "_hf_hook"):
                encoder._hf_hook.io_same_device = True
            else:
                add_hook_to_module(encoder, AlignDevicesHook(io_same_device=True))

        # 2. Prepare encoder args and encoder kwargs from model kwargs.
        irrelevant_prefix = ["decoder_", "cross_attn", "use_cache"]
        encoder_kwargs = {
            argument: value
            for argument, value in model_kwargs.items()
            if not any(argument.startswith(p) for p in irrelevant_prefix)
        }
        encoder_signature = set(inspect.signature(encoder.forward).parameters)
        encoder_accepts_wildcard = "kwargs" in encoder_signature or "model_kwargs" in encoder_signature
        if not encoder_accepts_wildcard:
            encoder_kwargs = {
                argument: value for argument, value in encoder_kwargs.items() if argument in encoder_signature
            }

        # 3. make sure that encoder returns `ModelOutput`
        model_input_name = model_input_name if model_input_name is not None else self.main_input_name
        encoder_kwargs["return_dict"] = True
        encoder_kwargs[model_input_name] = inputs_tensor

        if "input_visual_features" in model_kwargs:
            encoder_kwargs["img_features"] = model_kwargs["input_visual_features"]

        model_kwargs["encoder_outputs"]: ModelOutput = encoder(**encoder_kwargs)
        if "input_visual_features" in model_kwargs:
            model_kwargs["encoder_outputs"].last_hidden_state = model_kwargs["encoder_outputs"].last_hidden_state[:, 1:]

        return model_kwargs

